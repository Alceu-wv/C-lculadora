namespace Logica
{
    public class Operacoes
    {
        public float Soma(float num1, float num2)
        {
            return num1 + num2;
        }

        public float Multiplica(float num1, float num2)
        {
            return (num1 * num2);
        }

        public float Divide(float num1, float num2)
        {
            return (num1 / num2);
        }

        public float Subtrai(float num1, float num2)
        {
            return (num1 - num2);
        }
    }
}